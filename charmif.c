{
    FILE *fp;
    int i;
    uint16_t ram[4096];

    
    for(i=0;i<4096;i++)
    {
        ram[i] = 0;
    }

    for(i=0;i<16;i++)
    {
        ram[2032+i] = colours[i];
    }

    for(i=0;i<1024;i++)
    {
        ram[2048+i] = char_rom[i<<1] << 8 | char_rom[(i<<1)+1];
    }


    fp = fopen("vram.mif","w");
    fprintf(fp, "DEPTH = 4096;\n");
    fprintf(fp, "WIDTH = 16;\n");
    fprintf(fp, "ADDRESS_RADIX = HEX;\n");
    fprintf(fp, "DATA_RADIX = HEX;\n");
    fprintf(fp, "CONTENT\nBEGIN\n");
    for(i=0;i<4096;i++)
    {
        fprintf(fp, "%03X : %04X ;\n",i,ram[i]);
    }
    fprintf(fp, "END;\n");
    fclose(fp);
    printf("MIF file created\n");

    fclose(fp);
}

